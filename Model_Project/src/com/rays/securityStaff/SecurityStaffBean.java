package com.rays.securityStaff;

public class SecurityStaffBean {

	private long securityStaffId;
	private String staffName;
	private String shift;
	private double salary;

	public long getSecurityStaffId() {
		return securityStaffId;
	}

	public void setSecurityStaffId(long securityStaffId) {
		this.securityStaffId = securityStaffId;
	}

	public String getStaffName() {
		return staffName;
	}

	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}

	public String getShift() {
		return shift;
	}

	public void setShift(String shift) {
		this.shift = shift;
	}

	public double getSalary() {
		return salary;
	}

	public void setSalary(double salary) {
		this.salary = salary;
	}

}
